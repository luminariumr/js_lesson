const num1 = 10;
const num2 = 50;

let val;

// Simple math with numbers
val = num1 + num2;
val = num1 - num2;
val = num1 * num2;
val = num1 / num2;
val = num1 % num2;

console.log(val);

// Math Object
val = Math.PI; // PI number
console.log(val);
val = Math.E; // Euler's number
console.log(val);
val = Math.round(2.4); // Round Number
console.log(val);
val = Math.ceil(2.6); // Ceiling
console.log(val);
val = Math.floor(2.4); // Floor
console.log(val);
val = Math.sqrt(64); // Square Root
console.log(val);
val = Math.abs(-3); // Absolute Number
console.log(val);
val = Math.pow(8,3); // Power of
console.log(val);
val = Math.min(2,33,4,1,55,6,3,-2); // Minimum
console.log(val)
val = Math.random(); // Random Number
console.log(val)
val = Math.floor(Math.random() * 20); // Random from 0-19 without float but with +1 = 1-20
